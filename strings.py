my_string = "I have a black car."

#print the string
print(my_string)

#indexing the string
print(my_string[1])

#finding out string length
print(len(my_string))

#looping through a string
for a in my_string:
    print(a)

#check if an entity is present in a string
print("black" in my_string)

#slicing strings
print(my_string[2:5])

print(my_string[-7:-1])