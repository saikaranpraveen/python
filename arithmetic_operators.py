x = 200
y = 20

#addition
z = x + y
print (str(x) + " plus " + str(y) + " is " + str(z))

#subtraction
z = x - y
print (str(x) + " minus " + str(y) + " is " + str(z))

#multiplication
z = x * y
print (str(x) + " times " + str(y) + " is " + str(z))

#division
z = x / y
print (str(x) + " divided by " + str(y) + " is " + str(z))

#modulus
z = x % y
print ("The remainder when " + str(x) + " is divided by " + str(y) + " is " + str(z) )

#exponential
z = x ** y
print (str(x) + " to the times " + str(y) + " is " + str(z))